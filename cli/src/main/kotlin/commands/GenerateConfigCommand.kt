package org.noelware.hazel.cli.commands

import com.github.ajalt.clikt.core.CliktCommand
import com.github.ajalt.clikt.parameters.arguments.argument
import com.github.ajalt.clikt.parameters.types.file
import com.github.ajalt.mordant.terminal.Terminal
import org.noelware.hazel.cli.logger
import java.io.File

class GenerateConfigCommand(private val terminal: Terminal): CliktCommand(
    "Generates a configuration file in the specified destination",
    name = "generate-config"
) {
    private val dest: File by argument("dest").file(
        mustExist = false,
        canBeFile = true,
        canBeDir = false,
        mustBeWritable = true,
        mustBeReadable = true,
        canBeSymlink = true
    )

    override fun run() {
        terminal.logger.info("Writing configuration file in $dest")
        dest.writeBytes(
            """
        |# This configuration file was generated by `hazel generate-config`
        |storage:
        |  filesystem:
        |    directory: /var/lib/noelware/hazel/data
        """.trimMargin("|").toByteArray()
        )
    }
}
